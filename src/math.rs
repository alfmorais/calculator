pub fn addition(number_a: i32, number_b: i32) -> () {
    let result = number_a + number_b;

    print!("O resultado da soma: {result}\n");
}

pub fn subtraction(number_a: i32, number_b: i32) -> () {
    let result = number_a - number_b;

    print!("O resultado da subtração: {result}\n");
}

pub fn multiplication(number_a: i32, number_b: i32) -> () {
    let result = number_a * number_b;

    print!("O resultado da multiplicação: {result}\n");
}

pub fn division(number_a: i32, number_b: i32) -> () {
    let result = number_a / number_b;

    print!("O resultado da divisão: {result}\n");
}